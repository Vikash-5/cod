 public class Student
{
    String name;
    String roll;
    String grade;
    public Student(String n,String r,String g)
    {
        name=n;
        roll=r;
        grade=g;
    }
    public void displayId()
    {
        System.out.println("Student name:"+name);
        System.out.println("Student roll no:"+roll);
        System.out.println("Student grade:"+grade);
    }
}
    

